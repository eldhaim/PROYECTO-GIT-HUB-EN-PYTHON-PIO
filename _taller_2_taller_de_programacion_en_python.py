#Ejercicio 1: Personalizar Mensajes de Bienvenida
def mensaje_bienvenida(edad):
    if edad >= 18:
        print("¡Felicidades! Eres mayor de edad.")
    else:
        años_faltan = 18 - edad
        print(f"Aún eres menor de edad. Te faltan {años_faltan} años para ser mayor.")

# Ejemplo de uso
edad = int(input("Ingresa tu edad: "))
mensaje_bienvenida(edad)

#Ejercicio 2: Determinando la Generación
def determinar_generacion(edad):
    if edad >= 58:
        return "Baby Boomer"
    elif 42 <= edad < 58:
        return "Generación X"
    elif 26 <= edad < 42:
        return "Millennial"
    elif 10 <= edad < 26:
        return "Generación Z"
    else:
        return "Generación Alfa"

def mensaje_bienvenida_con_generacion(edad):
    generacion = determinar_generacion(edad)
    if edad >= 18:
        print(f"¡Felicidades! Eres mayor de edad y perteneces a la {generacion}.")
    else:
        años_faltan = 18 - edad
        print(f"Aún eres menor de edad. Te faltan {años_faltan} años para ser mayor y perteneces a la {generacion}.")

# Ejemplo de uso
edad = int(input("Ingresa tu edad: "))
mensaje_bienvenida_con_generacion(edad)

